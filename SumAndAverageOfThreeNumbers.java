class SumAndAverageOfThreeNumbers{
    public static void main(String[] args){
        int a = 10;
        int b = 25;
        int c =56;

        int sum = a + b + c;

        double avg = (double)sum / 3;

        System.out.println("The sum of three numbers is : " + sum);
        System.out.println("The average of three numbers is : " + avg);

    }
}